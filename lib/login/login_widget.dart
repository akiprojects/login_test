  void signInWithNaver() async {
    try {
      final NaverLoginResult result = await FlutterNaverLogin.logIn();

      if (result.status == NaverLoginStatus.loggedIn) {
        print('Naver AccessToken: ${result.accessToken}');
        print('Naver User ID: ${result.account.id}');
        print('Naver User Email: ${result.account.email}');
        print('Naver User Name: ${result.account.name}');

        // 로그인 성공 후 홈 페이지로 이동
        MyApp.of(context).login();  // 로그인 성공 처리
      } else {
        print('Naver Login Failed: ${result.status}');
      }
    } catch (error) {
      print('Naver Login Error: $error');
    }
  }

